				<p style="padding-top:0px; margin-top:0px;">
				<strong style="color:deeppink;"><a href="http://blacklog.mitplw.com/" target="blank">Luis Blackaller</a></strong> is an emerging film <strong>Director</strong> with a multidisciplinary background that covers aspects of entertainment, science, design, art and visual storytelling. He earned a BS with honors as a mathematician in the <strong>UNAM</strong>, and has been trained as an animator in the <strong><a href="http://www.vfs.com/blog/2008/07/11/vfs-to-mit/" target="blank">Vancouver Film School</a></strong>.
				</p> 
				<p style="">
				With more than 10 years of experience in the Mexican film and television industry, Luis has worked as a designer, art director, motion graphics artist and storyboard artist among academy award winners in films like <strong><a href="http://www.imdb.com/title/tt0245712/" target="blank">Amores Perros</a></strong> and <strong><a href="http://www.imdb.com/title/tt0449467/" target="blank">Babel</a></strong>.
				</p> 
				<p style="">
				In 2008, Luis earned a MS from the <strong> MIT <a href="http://www.media.mit.edu/" target="blank">Media Lab</a></strong>  under the mentorship of John Maeda, where he explored online creative social systems and their relationship with artistic expression and communication.
				</p> 
				<p style="">
				After graduating from MIT, Luis worked as a <strong>Producer</strong> and <strong>Creative Director</strong> for the Next Billion Network at the MIT Media Lab, where he designed and supervised the execution of alternative strategies of communication and promotion of research, directing a team of documentary videographers and web developers for more than a year.
				</p> 
